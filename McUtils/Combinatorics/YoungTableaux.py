    def __init__(self, base_int, tableaxu_cache=None, subset_cache=None):
        """
        **LLM Docstring**

        Initialize a standard Young-tableau generator and its caches.

        When explicit caches are not supplied, the class-level tableau and subset caches are reused across instances.

        :param base_int: integer whose partitions define the tableau sizes generated by this instance
        :type base_int: int
        :param tableaxu_cache: optional mapping from partition tuples to cached tableau arrays
        :type tableaxu_cache: dict | None
        :param subset_cache: optional mapping from partition-size tuples to cached unique subsets
        :type subset_cache: dict | None
        :return: `None`
        :rtype: None
        """
        self.base = base_int
        if tableaxu_cache is None:
            tableaxu_cache = self.tableaxu_cache
        self.cache = tableaxu_cache
        if subset_cache is None:
            subset_cache = self.subset_cache
        self.subset_cache = subset_cache

    def number_of_tableaux(self, partitions=None, **partition_opts):
        """
        **LLM Docstring**

        Count standard Young tableaux over one or more partitions of the instance base.

        If `partitions` is omitted, integer partitions of `self.base` are generated. A single partition is wrapped, while nested partition groups are flattened before summing the hook-length counts.

        :param partitions: a single partition, a flat collection of partitions, or nested partition groups
        :type partitions: array-like | None
        :param partition_opts: options forwarded to `IntegerPartitioner.partitions` when partitions are generated
        :type partition_opts: Any
        :return: total number of standard tableaux for all selected partitions
        :rtype: int
        """
        if partitions is None:
            partitions = IntegerPartitioner.partitions(self.base, **partition_opts)

        smol = nput.is_numeric(partitions[0])
        if smol:
            partitions = [partitions]

        if not nput.is_numeric(partitions[0][0]):
            partitions = itertools.chain(*partitions)
        else:
            partitions = partitions
        return sum(self.count_standard_tableaux(p) for p in partitions)
    def get_standard_tableaux(self, partitions=None, *, symbols=None,  brute_force=False,
                              return_partitions=False,
                              **partition_opts):
        """
        **LLM Docstring**

        Generate standard Young tableaux for selected partitions.

        The method normalizes single, flat, and nested partition inputs, then delegates each partition to `standard_partition_tableaux`. When requested, it returns the normalized partitions alongside the generated row-array representation.

        :param partitions: a single partition, flat partition collection, or nested partition groups; defaults to partitions of `self.base`
        :type partitions: array-like | None
        :param symbols: optional values used to replace the default integer labels by indexed lookup
        :type symbols: array-like | None
        :param brute_force: use permutation filtering instead of the recursive construction
        :type brute_force: bool
        :param return_partitions: return the normalized partition specification with the tableaux
        :type return_partitions: bool
        :param partition_opts: options forwarded to `IntegerPartitioner.partitions`
        :type partition_opts: Any
        :return: generated tableau arrays, optionally paired with their partitions
        :rtype: list[numpy.ndarray] | tuple[list[numpy.ndarray], array-like]
        """
        if partitions is None:
            partitions = IntegerPartitioner.partitions(self.base, **partition_opts)

        smol = nput.is_numeric(partitions[0])
        if smol:
            partitions = [partitions]

        if not nput.is_numeric(partitions[0][0]):
            part_iter = itertools.chain(*partitions)
            if return_partitions:
                partitions = list(part_iter)
                part_iter = partitions
        else:
            part_iter = partitions

        tabs = [
            self.standard_partition_tableaux(partition,
                                             cache=self.cache,
                                             subset_cache=self.subset_cache,
                                             brute_force=brute_force, symbols=symbols)
            for partition in part_iter
        ]
        if smol:
            tabs = tabs[0]

        if return_partitions:
            if smol:
                partitions = partitions[0]
            return tabs, partitions
        else:
            return tabs

    @classmethod
    def standard_partition_tableaux_bf(cls, partition, unique_perms=False, concatenate=False):
        """
        **LLM Docstring**

        Enumerate tableaux for a partition by brute-force permutation filtering.

        Rows are represented as separate arrays whose first axis indexes candidate tableaux. Candidates are retained only when every row and every transposed column is nondecreasing. With `unique_perms=True`, row-membership permutations are used to derive rank positions; otherwise all permutation indices of `sum(partition)` are considered.

        :param partition: row lengths of the Young diagram
        :type partition: sequence[int]
        :param unique_perms: derive candidates from unique permutations of repeated row labels
        :type unique_perms: bool
        :param concatenate: concatenate row arrays along the column axis before returning
        :type concatenate: bool
        :return: filtered tableau rows or their concatenation
        :rtype: list[numpy.ndarray] | numpy.ndarray
        """
        if unique_perms:
            n = len(partition)
            perm_list = sum(([n - i] * k for i, k in enumerate(partition)), [])
            #
            perms = UniquePermutations(perm_list[1:]).permutations()
            perms = np.concatenate([
                np.full((len(perms), 1), n, dtype=perms.dtype),
                perms
            ], axis=1)
            idx_pos = np.argsort(np.argsort(-perms, axis=1))
        else:
            idx_pos = nput.permutation_indices(sum(partition), sum(partition))

        tableaux = np.array_split(idx_pos, np.cumsum(partition)[:-1], axis=1)
        valid = np.full(len(idx_pos), True)
        for i, t in enumerate(zip(*tableaux)):
            if any(len(tt) > 1 and (np.diff(tt) < 0).any() for tt in t):
                valid[i] = False
            if valid[i] and any(len(tt) > 1 and (np.diff(tt) < 0).any() for tt in itut.transpose(t)):
                valid[i] = False

        tableaux = [t[valid] for t in tableaux]
        if concatenate:
            tableaux = np.concatenate(tableaux, axis=1)

        # if return_perms:
        #     return perms[valid], tableaux
        # else:
        return tableaux

    @classmethod
    def populate_sst_frames(cls, partition, frame, segment_lists):
        """
        **LLM Docstring**

        Fill tableau frame shapes from segmented symbol selections.

        For each entry in `segment_lists`, the function allocates one integer row per partition row and copies successive slices from each segment according to the counts stored in `frame`. Segment offsets are tracked independently for each segment index.

        :param partition: row lengths used to allocate each output subframe
        :type partition: sequence[int]
        :param frame: per-row counts describing how many values to take from each segment
        :type frame: sequence[sequence[int]]
        :param segment_lists: iterable of segment collections supplying the values
        :type segment_lists: iterable[sequence[array-like]]
        :return: list of populated subframes, each represented as a list of row arrays
        :rtype: list[list[numpy.ndarray]]
        """
        frame_list = []
        for segments in segment_lists:
            splits = [0] * len(partition)
            subframe = [np.zeros(k, dtype=int) for k in partition]
            for s, r in zip(subframe, frame):
                k = 0
                for n, i in enumerate(r):
                    j = splits[n]
                    s[k:k + i] = segments[n][j:j + i]
                    splits[n] += i
                    k += i
            frame_list.append(subframe)
        return frame_list

    @classmethod
    def standard_partitions(cls, partition):
        """
        **LLM Docstring**

        Generate valid two-dimensional partition frames and cumulative offsets.

        `IntegerPartitioner2D.get_partitions` constructs candidate arrays. Candidates are retained when cumulative offsets are nonincreasing down every column.

        :param partition: one-dimensional partition used as both source and target dimensions
        :type partition: sequence[int]
        :return: valid partition frames and their cumulative offsets
        :rtype: list[numpy.ndarray]
        """
        #TODO: make this more efficient
        base_partitions = IntegerPartitioner2D.get_partitions(partition, partition)
        offsets = np.cumsum(base_partitions, axis=-1)
        valid = np.all(np.all(np.diff(offsets, axis=-2) <= 0, axis=-2), axis=-1)

        return [
            base_partitions[valid,],
            offsets[valid,],
        ]

    @classmethod
    def hook_numbers(cls, partition):
        """
        **LLM Docstring**

        Compute hook lengths for every cell of a Young diagram.

        Cells are traversed row by row. Each hook length is the number of cells from the current position through the end of its row plus one for every lower row extending into the same column.

        :param partition: row lengths of the Young diagram
        :type partition: sequence[int]
        :return: hook lengths in row-major order
        :rtype: list[int]
        """
        return [
            (p - j) + sum(
                1 if pk >= (j+1) else 0
                for pk in partition[i + 1:]
            )
            for i, p in enumerate(partition)
            for j in range(p)
        ]

    @classmethod
    def count_standard_tableaux(cls, partition):
        """
        **LLM Docstring**

        Count standard tableaux using the hook-length formula.

        The function computes `n! / product(hooks)` as a product of elementwise ratios, rounds the floating-point result, and converts it to an integer NumPy scalar.

        :param partition: row lengths of the Young diagram
        :type partition: sequence[int]
        :return: number of standard Young tableaux of the given shape
        :rtype: numpy.integer
        """
        nums = np.arange(1, np.sum(partition)+1)
        denoms = np.sort(cls.hook_numbers(partition))
        return np.round(np.prod(nums/denoms)).astype(int)

