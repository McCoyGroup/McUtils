    @classmethod
    def _sst_2(cls, partition, cache=None, subset_cache=None, symbols=None):
        """
        **LLM Docstring**

        Recursively construct standard tableaux for one partition.

        Small partitions are handled by explicit arrays. Larger partitions are decomposed into valid two-dimensional frames, unique symbol subsets are assigned to frame components, and recursive tableaux are combined. Candidate rows are filtered for cross-row ordering, completeness, and duplicate Lehmer codes before being cached under the partition tuple. The cached representation is a list of row arrays with shape `(n_tableaux, row_length)`.

        :param partition: row lengths of the target Young diagram
        :type partition: sequence[int]
        :param cache: optional tableau cache keyed by partition tuples
        :type cache: dict | None
        :param subset_cache: optional unique-subset cache keyed by component-size tuples
        :type subset_cache: dict | None
        :param symbols: optional labels to place in the tableaux instead of `0..sum(partition)-1`
        :type symbols: array-like | None
        :return: one array per tableau row, each containing all generated tableaux along axis 0
        :rtype: list[numpy.ndarray]
        """
        tableaux = None
        if len(partition) == 1:
            tableaux = [
                np.arange(partition[0], dtype=int)[np.newaxis]
            ]
        elif sum(partition) == 2:
            tableaux = [
                np.array([[0]], dtype=int),
                np.array([[1]], dtype=int)
            ]
        elif sum(partition) == 3:
            if partition[0] == 2:
                tableaux = [
                    np.array([[0, 1], [0, 2]], dtype=int),
                    np.array([[2], [1]], dtype=int)
                ]
            else:
                tableaux = [
                    np.array([[0]], dtype=int),
                    np.array([[1]], dtype=int),
                    np.array([[2]], dtype=int),
                ]
        if tableaux is not None:
            if symbols is not None:
                symbols = np.asanyarray(symbols)
                return [
                    symbols[tab]
                    for tab in tableaux
                ]

            return tableaux

        if cache is None:
            cache = {}
        if subset_cache is None:
            subset_cache = {}
        partition = tuple(partition)
        if partition in cache:
            tableaux = cache[partition]
            if symbols is not None:
                symbols = np.asanyarray(symbols)
                return [
                    symbols[tab]
                    for tab in tableaux
                ]

            return tableaux
        else:
            frames, offsets = cls.standard_partitions(partition)
            offsets[:, :, 1:] = offsets[:, :, :-1]
            offsets[:, :, 0] = 0

            if symbols is None:
                symbols = np.arange(np.sum(partition))
            segments = np.array_split(symbols, np.cumsum(partition)[:-1])

            # n_frames = 0
            tableaux_generators = []
            for f, o in zip(frames, offsets):
                # print("!", f, o)
                # nsubs = 1
                subframes = []
                for pp, oo, seg in zip(f.T, o.T, segments):
                    # print("|||", pp, oo, seg)
                    frame_splits = cls.split_frame(pp, oo)
                    # for p,o in frame_splits:
                    #     print(">", p)
                    partition_sizes = [
                        np.sum(p) for p, o in frame_splits
                    ]

                    tp_key = tuple(partition_sizes)
                    subsets = subset_cache.get(tp_key)
                    if subsets is None:
                        subsets = UniqueSubsets.unique_subsets(partition_sizes)
                        subset_cache[tp_key] = subsets

                    comb_splits = np.array_split(
                        subsets,
                        np.cumsum(partition_sizes)[:-1],
                        axis=1
                    )
                    # print(">><<>><<", partition_sizes)
                    # print(subsets)
                    subssts = []
                    for spl in zip(*comb_splits):
                        subrow = []
                        # print('...', spl)
                        for (p, o), ss in zip(frame_splits, spl):
                            if len(ss) == 0: continue
                            # print(":", seg[ss])
                            p_idx = [x for x, p0 in enumerate(p) if p0 > 0]
                            subsubssts = cls._sst_2([p[x] for x in p_idx], cache=cache, symbols=seg[ss])
                            reshaped_ssts = []
                            for ss in zip(*subsubssts):
                                p_full = [[]] * len(p)
                                for x, r in zip(p_idx, ss):
                                    p_full[x] = r
                                reshaped_ssts.append([o, p_full])
                            subrow.append(reshaped_ssts)
                        # subrow_joins = []
                        # for r in itertools.product(*subrow):
                        #     joins = [
                        #         np.concatenate([rr[1][i] for rr in r])
                        #         for i in range(len(r[0]))
                        #     ]
                        #     print(joins)
                        #     if all(np.all(j == np.sort(j)) for j in joins):
                        #         subrow_joins.append(r)
                        # subssts.append(subrow_joins)#
                        subssts.append(list(itertools.product(*subrow)))
                        # nsubs *= len(subssts[-1])

                    subframes.append(subssts)
                # n_frames += nsubs
                tableaux_generators.append(subframes)

            n_frames = sum(
                np.prod([
                    len(f)
                    for f in frame_list
                ])
                for subframes in tableaux_generators
                for frame_list in itertools.product(*subframes)
            )

            mask = np.full(n_frames, True)
            tableaux = [np.full((n_frames, p), -1, dtype=int) for p in partition]
            n = 0
            for subframes in tableaux_generators:
                for frame_list in itertools.product(*subframes):
                    for frame_choice in itertools.product(*frame_list):
                        for f in frame_choice:
                            for oo, p_sets in f:
                                if not mask[n]: break
                                for i, (o, ss) in enumerate(zip(oo, p_sets)):
                                    k = len(ss)
                                    if k > 0:
                                        for j in range(i):
                                            if np.any(tableaux[j][n, o:o + k] >= ss):
                                                mask[n] = False
                                                break
                                        if not mask[n]: break
                                        tableaux[i][n, o:o + k] = ss
                        n += 1

            tableaux = [
                t[mask, :]
                for t in tableaux
            ]

            mask = np.all([
                np.all(t > -1, axis=1)
                for t in tableaux
            ], axis=0)
            tableaux = [
                t[mask, :]
                for t in tableaux
            ]


            codes = lehmer_encode(np.concatenate(tableaux, axis=-1))
            _, mask = np.unique(codes, return_index=True)
            tableaux = [
                t[mask, :]
                for t in tableaux
            ]

            cache[partition] = tableaux

            return tableaux
    @classmethod
    def standard_partition_tableaux(cls, partition,
                                    cache=None,
                                    subset_cache=None,
                                    symbols=None, brute_force=False):
        """
        **LLM Docstring**

        Generate all standard tableaux for a single partition.

        The method selects either brute-force permutation filtering or the recursive `_sst_2` implementation. If `symbols` is provided, each integer index in every row array is replaced by the corresponding symbol.

        :param partition: row lengths of the Young diagram
        :type partition: sequence[int]
        :param cache: optional recursive tableau cache
        :type cache: dict | None
        :param subset_cache: optional unique-subset cache
        :type subset_cache: dict | None
        :param symbols: optional label array indexed by generated tableau entries
        :type symbols: array-like | None
        :param brute_force: use `standard_partition_tableaux_bf` instead of `_sst_2`
        :type brute_force: bool
        :return: list of row arrays with the tableau index along axis 0
        :rtype: list[numpy.ndarray]
        """
        if brute_force:
            tableaux = cls.standard_partition_tableaux_bf(partition, concatenate=False, unique_perms=False)
        else:
            tableaux = cls._sst_2(partition, cache=cache, subset_cache=subset_cache)
        if symbols is not None:
            symbols = np.asanyarray(symbols)
            tableaux = [
                symbols[tab]
                for tab in tableaux
            ]
        return tableaux

    @classmethod
    def print_tableaux(cls, tableaux):
        """
        **LLM Docstring**

        Format and print one or more tableau collections.

        A raw row-array collection is wrapped as a single collection. Rows are zipped by tableau index, formatted with `TableFormatter`, and preceded by a horizontal separator matching the first rendered line width.

        :param tableaux: tableau row arrays or a list of such collections
        :type tableaux: list[numpy.ndarray] | list[list[numpy.ndarray]]
        :return: `None`; output is written to standard output
        :rtype: None
        """
        from ..Formatters import TableFormatter
        if isinstance(tableaux[0], np.ndarray):
            tableaux = [tableaux]
        for tabs in tableaux:
            for sst in zip(*tabs):
                tab = TableFormatter("").format(sst)
                print("-"*len(tab.split("\n")[0]))
                print(tab)